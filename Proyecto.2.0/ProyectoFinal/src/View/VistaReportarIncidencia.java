        String mensaje = txtMensaje.getText().trim();
        if (mensaje.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor escriba un mensaje.");
            return;
        }
        
        // Llamar al controlador (que internamente usa usuarioLogeado)
        String resultado = controladora.reportarIncidencia(mensaje);
        
        JOptionPane.showMessageDialog(this, resultado);
        
        // Regresar al men√∫
        VistaOpcionesPracticante v = new VistaOpcionesPracticante(controladora);
        v.setVisible(true);
        this.dispose();
